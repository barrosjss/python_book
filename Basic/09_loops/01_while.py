#!/usr/bin/env python3

# While:
# Se utiliza cuando no sabemos de antemano cuántas veces se ejecutará el código, pues depende de una condición lógica.

# Imprimir números del 1 al 5

contador=1

while contador<=5:
    print(f"Número: {contador}")
    contador+=1

# Inicio: 1
# Continuo: 2
# Continuo: 3
# Continuo: 4
# Continuo: 5
# Fin

